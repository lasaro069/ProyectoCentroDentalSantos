package co.edu.unbosque.CentroDentalSantosFrontEnd;

public class OrdenesTrabajo {
	
	private String numero_orden;
	private String fecha_orden;
	private String cedula_paciente;
	private String cedula_medico;
	private String clase_trabajo;
	public String getNumero_orden() {
		return numero_orden;
	}
	public void setNumero_orden(String numero_orden) {
		this.numero_orden = numero_orden;
	}
	public String getFecha_orden() {
		return fecha_orden;
	}
	public void setFecha_orden(String fecha_orden) {
		this.fecha_orden = fecha_orden;
	}
	public String getCedula_paciente() {
		return cedula_paciente;
	}
	public void setCedula_paciente(String cedula_paciente) {
		this.cedula_paciente = cedula_paciente;
	}
	public String getCedula_medico() {
		return cedula_medico;
	}
	public void setCedula_medico(String cedula_medico) {
		this.cedula_medico = cedula_medico;
	}
	public String getClase_trabajo() {
		return clase_trabajo;
	}
	public void setClase_trabajo(String clase_trabajo) {
		this.clase_trabajo = clase_trabajo;
	}
	
	

}
